package operadores;
public class operadores1{
    public static void main(String[] args) {
        // Operadores relacionaiss
        int a = 10;
        int b = 2;
        if(a == b){
            System.out.println("==");
        }
        else if(a != b){
            System.out.println("!=");
        }
        else if(a < b){
            System.out.println("<");
        }
        else if(a > b){
            System.out.println(">");
        }
        else if(a <= b){
            System.out.println("<=");
        }
        else if(a >= b){
            System.out.println(">=");
        }



    }
}